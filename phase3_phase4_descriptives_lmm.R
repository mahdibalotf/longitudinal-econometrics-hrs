pairwise_groups <- function(group) {
  levs <- levels(droplevels(group))
  if (length(levs) < 2) {
    return(matrix(character(0), ncol = 2))
  }
  combn(levs, 2, simplify = TRUE)
}

continuous_pairwise_smd <- function(x, group) {
  keep <- !is.na(x) & !is.na(group)
  x <- x[keep]
  group <- droplevels(group[keep])

  pairs <- pairwise_groups(group)
  if (length(x) == 0 || ncol(pairs) == 0) {
    return(NA_real_)
  }

  smds <- purrr::map_dbl(seq_len(ncol(pairs)), function(j) {
    x1 <- x[group == pairs[1, j]]
    x2 <- x[group == pairs[2, j]]
    pooled_sd <- sqrt((var(x1, na.rm = TRUE) + var(x2, na.rm = TRUE)) / 2)

    if (is.na(pooled_sd) || pooled_sd == 0) {
      return(0)
    }

    abs((mean(x1, na.rm = TRUE) - mean(x2, na.rm = TRUE)) / pooled_sd)
  })

  max(smds, na.rm = TRUE)
}

binary_indicator_pairwise_smd <- function(indicator, group) {
  keep <- !is.na(indicator) & !is.na(group)
  indicator <- as.integer(indicator[keep])
  group <- droplevels(group[keep])

  pairs <- pairwise_groups(group)
  if (length(indicator) == 0 || ncol(pairs) == 0) {
    return(NA_real_)
  }

  smds <- purrr::map_dbl(seq_len(ncol(pairs)), function(j) {
    p1 <- mean(indicator[group == pairs[1, j]], na.rm = TRUE)
    p2 <- mean(indicator[group == pairs[2, j]], na.rm = TRUE)
    pooled_p_var <- ((p1 * (1 - p1)) + (p2 * (1 - p2))) / 2

    if (is.na(pooled_p_var) || pooled_p_var == 0) {
      return(0)
    }

    abs((p1 - p2) / sqrt(pooled_p_var))
  })

  max(smds, na.rm = TRUE)
}

categorical_level_smds <- function(x, group) {
  x <- fct_na_value_to_level(as.factor(x), level = "Missing")
  levels_x <- levels(x)

  tibble(
    level = levels_x,
    level_smd = purrr::map_dbl(levels_x, ~ binary_indicator_pairwise_smd(x == .x, group))
  )
}

anova_p_value <- function(data, variable, group_var) {
  model_data <- data %>%
    select(all_of(c(variable, group_var))) %>%
    filter(!is.na(.data[[variable]]), !is.na(.data[[group_var]]))

  if (n_distinct(model_data[[group_var]]) < 2 || nrow(model_data) == 0) {
    return(NA_real_)
  }

  fit <- lm(reformulate(group_var, response = variable), data = model_data)
  stats::anova(fit)[[5]][1]
}

chisq_p_value <- function(data, variable, group_var) {
  x <- fct_na_value_to_level(as.factor(data[[variable]]), level = "Missing")
  g <- data[[group_var]]
  tab <- table(x, g, useNA = "no")

  if (nrow(tab) < 2 || ncol(tab) < 2) {
    return(NA_real_)
  }

  suppressWarnings(stats::chisq.test(tab)$p.value)
}

summarise_continuous_for_table1 <- function(data, variable, label, group_var) {
  groups <- levels(data[[group_var]])
  group_values <- purrr::map_chr(groups, function(g) {
    format_mean_sd(data[[variable]][data[[group_var]] == g])
  })

  names(group_values) <- menopause_group_labels[groups]

  tibble(
    Characteristic = label,
    Level = "Mean (SD)",
    Overall = format_mean_sd(data[[variable]]),
    Missing = format(sum(is.na(data[[variable]])), big.mark = ","),
    `P value` = format_p(anova_p_value(data, variable, group_var)),
    `Max pairwise SMD` = format_smd(continuous_pairwise_smd(data[[variable]], data[[group_var]])),
    !!!as.list(group_values)
  ) %>%
    relocate(all_of(unname(menopause_group_labels[groups])), .after = Overall)
}

summarise_categorical_for_table1 <- function(data, variable, label, group_var) {
  groups <- levels(data[[group_var]])
  x <- fct_na_value_to_level(as.factor(data[[variable]]), level = "Missing")
  level_smds <- categorical_level_smds(x, data[[group_var]])
  covariate_smd <- max(level_smds$level_smd, na.rm = TRUE)
  p_value <- chisq_p_value(data, variable, group_var)
  n_total <- nrow(data)

  purrr::map_dfr(levels(x), function(level_i) {
    group_values <- purrr::map_chr(groups, function(g) {
      in_group <- data[[group_var]] == g
      denom <- sum(in_group, na.rm = TRUE)
      n_level <- sum(x[in_group] == level_i, na.rm = TRUE)
      format_n_pct(n_level, denom)
    })

    names(group_values) <- menopause_group_labels[groups]

    tibble(
      Characteristic = label,
      Level = as.character(level_i),
      Overall = format_n_pct(sum(x == level_i, na.rm = TRUE), n_total),
      Missing = "",
      `P value` = if_else(level_i == levels(x)[1], format_p(p_value), ""),
      `Max pairwise SMD` = if_else(level_i == levels(x)[1], format_smd(covariate_smd), ""),
      !!!as.list(group_values)
    ) %>%
      relocate(all_of(unname(menopause_group_labels[groups])), .after = Overall)
  })
}

lmer_fixed_effects_table <- function(model, model_name) {
  coefs <- as.data.frame(coef(summary(model))) %>%
    rownames_to_column("term") %>%
    as_tibble()

  p_col <- names(coefs)[str_detect(names(coefs), "^Pr\\(")]

  if (!"df" %in% names(coefs)) {
    coefs$df <- NA_real_
  }

  crit <- if_else(
    is.finite(coefs$df),
    qt(0.975, df = coefs$df),
    qnorm(0.975)
  )

  coefs %>%
    transmute(
      model = model_name,
      term = term,
      estimate = Estimate,
      std_error = `Std. Error`,
      df = df,
      conf_low = Estimate - crit * `Std. Error`,
      conf_high = Estimate + crit * `Std. Error`,
      statistic = `t value`,
      p_value = if (length(p_col) == 1) .data[[p_col]] else NA_real_
    )
}


lmer_model_diagnostics <- function(model, model_name, data) {
  convergence_messages <- model@optinfo$conv$lme4$messages
  if (is.null(convergence_messages)) {
    convergence_messages <- "None"
  }

  n_person_waves <- length(lme4::getME(model, "y"))
  model_aic <- AIC(model)
  model_bic <- BIC(model)
  model_log_likelihood <- as.numeric(logLik(model))
  model_deviance <- deviance(model)
  model_reml <- isREML(model)
  model_singular_fit <- isSingular(model, tol = 1e-4)

  tibble(
    model = model_name,
    n_person_waves = n_person_waves,
    n_persons = n_distinct(data[[id_var]]),
    aic = model_aic,
    bic = model_bic,
    log_likelihood = model_log_likelihood,
    deviance = model_deviance,
    reml = model_reml,
    singular_fit = model_singular_fit,
    convergence_messages = paste(convergence_messages, collapse = " | ")
  )
}

icc_from_random_slope_model <- function(model, model_name, age_c_values = c(-5, 0, 5, 10)) {
  vc <- as.data.frame(VarCorr(model))

  var_intercept <- vc %>%
    filter(grp == id_var, var1 == "(Intercept)", is.na(var2)) %>%
    pull(vcov)

  var_slope <- vc %>%
    filter(grp == id_var, var1 == "age_c65", is.na(var2)) %>%
    pull(vcov)

  cov_intercept_slope <- vc %>%
    filter(grp == id_var, var1 == "(Intercept)", var2 == "age_c65") %>%
    pull(vcov)

  residual_var <- vc %>%
    filter(grp == "Residual") %>%
    pull(vcov)

  if (length(var_intercept) == 0) var_intercept <- NA_real_
  if (length(var_slope) == 0) var_slope <- 0
  if (length(cov_intercept_slope) == 0) cov_intercept_slope <- 0
  if (length(residual_var) == 0) residual_var <- NA_real_

  tibble(age_c65 = age_c_values) %>%
    mutate(
      model = model_name,
      chronological_age = 65 + age_c65,
      between_person_variance =
        var_intercept +
        2 * age_c65 * cov_intercept_slope +
        (age_c65^2) * var_slope,
      residual_variance = residual_var,
      icc = between_person_variance / (between_person_variance + residual_variance),
      interpretation = if_else(
        age_c65 == 0,
        "Primary ICC at age 65 because age is centered at 65",
        "Age-specific ICC implied by random intercept-slope covariance"
      )
    ) %>%
    select(model, chronological_age, age_c65, between_person_variance, residual_variance, icc, interpretation)
}


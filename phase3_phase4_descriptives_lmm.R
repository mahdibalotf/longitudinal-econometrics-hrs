format_p <- function(p) {
  case_when(
    is.na(p) ~ "",
    p < 0.001 ~ "<0.001",
    TRUE ~ sprintf("%.3f", p)
  )
}

format_smd <- function(x) {
  case_when(
    is.na(x) ~ "",
    x < 0.001 ~ "<0.001",
    TRUE ~ sprintf("%.3f", x)
  )
}

format_n_pct <- function(n, denom) {
  if (is.na(denom) || denom == 0) {
    return("")
  }
  sprintf("%s (%.1f%%)", format(n, big.mark = ","), 100 * n / denom)
}

format_mean_sd <- function(x) {
  if (all(is.na(x))) {
    return("")
  }
  sprintf("%.2f (%.2f)", mean(x, na.rm = TRUE), sd(x, na.rm = TRUE))
}

format_median_iqr <- function(x) {
  if (all(is.na(x))) {
    return("")
  }
  qs <- quantile(x, probs = c(0.25, 0.5, 0.75), na.rm = TRUE, names = FALSE)
  sprintf("%.2f [%.2f, %.2f]", qs[2], qs[1], qs[3])
}

escape_html <- function(x) {
  x <- as.character(x)
  x <- replace_na(x, "")
  x <- gsub("&", "&amp;", x, fixed = TRUE)
  x <- gsub("<", "&lt;", x, fixed = TRUE)
  x <- gsub(">", "&gt;", x, fixed = TRUE)
  x <- gsub('"', "&quot;", x, fixed = TRUE)
  x
}


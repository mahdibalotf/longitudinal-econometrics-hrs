format_p <- function(p) {
  case_when(
    is.na(p) ~ "",
    p < 0.001 ~ "<0.001",
    TRUE ~ sprintf("%.3f", p)
  )
}

escape_html <- function(x) {
  x <- as.character(x)
  x <- replace_na(x, "")
  x <- gsub("&", "&amp;", x, fixed = TRUE)
  x <- gsub("<", "&lt;", x, fixed = TRUE)
  x <- gsub(">", "&gt;", x, fixed = TRUE)
  x <- gsub('"', "&quot;", x, fixed = TRUE)
  x
}

write_html_table <- function(data, path, title, subtitle = NULL) {
  header <- paste0("<th>", escape_html(names(data)), "</th>", collapse = "")

  body <- purrr::map_chr(seq_len(nrow(data)), function(i) {
    cells <- paste0("<td>", escape_html(unlist(data[i, ], use.names = FALSE)), "</td>", collapse = "")
    paste0("<tr>", cells, "</tr>")
  })

  html <- c(
    "<!doctype html>",
    "<html>",
    "<head>",
    "<meta charset=\"utf-8\">",
    paste0("<title>", escape_html(title), "</title>"),
    "<style>",
    "body { font-family: -apple-system, BlinkMacSystemFont, 'Segoe UI', sans-serif; margin: 28px; color: #172026; }",
    "h1 { font-size: 22px; margin-bottom: 4px; }",
    "p.subtitle { color: #52616b; margin-top: 0; max-width: 1100px; line-height: 1.4; }",
    "table { border-collapse: collapse; width: 100%; font-size: 13px; }",
    "th { background: #f3f6f8; text-align: left; position: sticky; top: 0; }",
    "th, td { border: 1px solid #d9e1e7; padding: 7px 9px; vertical-align: top; }",
    "tr:nth-child(even) td { background: #fbfcfd; }",
    ".note { margin-top: 16px; color: #52616b; font-size: 12px; max-width: 1100px; line-height: 1.45; }",
    "</style>",
    "</head>",
    "<body>",
    paste0("<h1>", escape_html(title), "</h1>"),
    if (!is.null(subtitle)) paste0("<p class=\"subtitle\">", escape_html(subtitle), "</p>") else NULL,
    "<table>",
    paste0("<thead><tr>", header, "</tr></thead>"),
    "<tbody>",
    body,
    "</tbody>",
    "</table>",
    "<p class=\"note\">Generated by phase5_phase6_ipcw_sensitivity.R.</p>",
    "</body>",
    "</html>"
  )

  writeLines(html, path)
}

bound_probability <- function(p, eps = 1e-4) {
  pmin(pmax(p, eps), 1 - eps)
}

glm_coefficients_table <- function(model, model_name) {
  coefs <- as.data.frame(coef(summary(model))) %>%
    rownames_to_column("term") %>%
    as_tibble()

  p_col <- names(coefs)[str_detect(names(coefs), "^Pr\\(")]

  coefs %>%
    transmute(
      model = model_name,
      term = term,
      estimate = Estimate,
      std.error = `Std. Error`,
      statistic = `z value`,
      p.value = if (length(p_col) == 1) .data[[p_col]] else NA_real_
    )
}

lmer_fixed_effects_table <- function(model, model_name) {
  coefs <- as.data.frame(coef(summary(model))) %>%
    rownames_to_column("term") %>%
    as_tibble()

  p_col <- names(coefs)[str_detect(names(coefs), "^Pr\\(")]

  if (!"df" %in% names(coefs)) {
    coefs$df <- NA_real_
  }

  crit <- if_else(
    is.finite(coefs$df),
    qt(0.975, df = coefs$df),
    qnorm(0.975)
  )

  coefs %>%
    transmute(
      model = model_name,
      term = term,
      estimate = Estimate,
      std_error = `Std. Error`,
      df = df,
      conf_low = Estimate - crit * `Std. Error`,
      conf_high = Estimate + crit * `Std. Error`,
      statistic = `t value`,
      p_value = if (length(p_col) == 1) .data[[p_col]] else NA_real_
    )
}

lmer_variance_components_table <- function(model, model_name) {
  as.data.frame(VarCorr(model)) %>%
    as_tibble() %>%
    transmute(
      model = model_name,
      group = grp,
      effect_1 = var1,
      effect_2 = var2,
      variance_or_covariance = vcov,
      sd_or_correlation = sdcor
    )
}

lmer_model_diagnostics <- function(model, model_name, data, weight_var = NA_character_) {
  convergence_messages <- model@optinfo$conv$lme4$messages
  if (is.null(convergence_messages)) {
    convergence_messages <- "None"
  }

  n_person_waves <- length(lme4::getME(model, "y"))
  model_aic <- AIC(model)
  model_bic <- BIC(model)
  model_log_likelihood <- as.numeric(logLik(model))
  model_deviance <- deviance(model)
  model_reml <- isREML(model)
  model_singular_fit <- isSingular(model, tol = 1e-4)

  tibble(
    model = model_name,
    n_person_waves = n_person_waves,
    n_persons = n_distinct(data[[id_var]]),
    aic = model_aic,
    bic = model_bic,
    log_likelihood = model_log_likelihood,
    deviance = model_deviance,
    reml = model_reml,
    singular_fit = model_singular_fit,
    weights = weight_var,
    convergence_messages = paste(convergence_messages, collapse = " | ")
  )
}

format_fixed_effects <- function(data) {
  data %>%
    mutate(
      estimate = sprintf("%.4f", estimate),
      std_error = sprintf("%.4f", std_error),
      df = sprintf("%.1f", df),
      conf_low = sprintf("%.4f", conf_low),
      conf_high = sprintf("%.4f", conf_high),
      statistic = sprintf("%.3f", statistic),
      p_value = format_p(p_value)
    )
}

extract_slope_differences <- function(fixed_effects) {
  fixed_effects %>%
    filter(str_detect(term, "menopause_group.*:age_c65|age_c65:menopause_group")) %>%
    filter(!str_detect(term, "education_c12|age_c65_sq")) %>%
    mutate(
      contrast = term %>%
        str_replace("menopause_group", "") %>%
        str_replace(":age_c65", "") %>%
        str_replace("age_c65:", "") %>%
        str_replace_all("`", "")
    ) %>%
    select(
      model,
      contrast,
      term,
      estimate,
      std_error,
      df,
      conf_low,
      conf_high,
      statistic,
      p_value
    )
}


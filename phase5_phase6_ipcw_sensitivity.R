        rahispan == 0 & raracem == 2 ~ "Non-Hispanic Black",
        rahispan == 0 & raracem == 3 ~ "Non-Hispanic Other",
        TRUE ~ "Missing/Unknown"
      ),
      race_ethnicity = factor(
        race_ethnicity,
        levels = c(
          "Non-Hispanic White",
          "Non-Hispanic Black",
          "Hispanic",
          "Non-Hispanic Other",
          "Missing/Unknown"
        )

    )
}

get_fixed_model_matrix <- function(model, newdata) {
  fixed_terms <- delete.response(terms(model, fixed.only = TRUE))
  contrasts_arg <- attr(lme4::getME(model, "X"), "contrasts")

  X <- model.matrix(
    fixed_terms,
    data = newdata,
    contrasts.arg = contrasts_arg
  )

  # Align columns exactly to fixed-effect vector order. If model.matrix drops
  # unused columns in a restricted grid, add them back as zero columns.
  beta_names <- names(lme4::fixef(model))
  missing_cols <- setdiff(beta_names, colnames(X))

  if (length(missing_cols) > 0) {
    X_missing <- matrix(0, nrow = nrow(X), ncol = length(missing_cols))
    colnames(X_missing) <- missing_cols
    X <- cbind(X, X_missing)
  }

  X[, beta_names, drop = FALSE]
}

linear_prediction_from_L <- function(model, L) {
  beta <- lme4::fixef(model)
  V <- as.matrix(vcov(model))
  estimate <- as.numeric(L %*% beta)
  se <- sqrt(as.numeric(L %*% V %*% t(L)))

  tibble(
    estimate = estimate,
    std_error = se,
    conf_low = estimate - qnorm(0.975) * se,
    conf_high = estimate + qnorm(0.975) * se
  )
}

average_design_vector <- function(model, covariate_distribution, menopause_group_value, age_value) {
  newdata <- covariate_distribution %>%
    mutate(
      menopause_group = factor(menopause_group_value, levels = menopause_group_levels),
      age_c65 = age_value - 65
    )

  colMeans(get_fixed_model_matrix(model, newdata))
}




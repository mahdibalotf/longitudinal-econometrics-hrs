format_p <- function(p) {
  case_when(
    is.na(p) ~ "",
    p < 0.001 ~ "<0.001",
    TRUE ~ sprintf("%.3f", p)
  )
}

escape_html <- function(x) {
  x <- as.character(x)
  x <- replace_na(x, "")
  x <- gsub("&", "&amp;", x, fixed = TRUE)
  x <- gsub("<", "&lt;", x, fixed = TRUE)
  x <- gsub(">", "&gt;", x, fixed = TRUE)
  x <- gsub('"', "&quot;", x, fixed = TRUE)
  x
}

write_html_table <- function(data, path, title, subtitle = NULL) {
  header <- paste0("<th>", escape_html(names(data)), "</th>", collapse = "")

  body <- purrr::map_chr(seq_len(nrow(data)), function(i) {
    cells <- paste0("<td>", escape_html(unlist(data[i, ], use.names = FALSE)), "</td>", collapse = "")
    paste0("<tr>", cells, "</tr>")
  })

  html <- c(
    "<!doctype html>",
    "<html>",
    "<head>",
    "<meta charset=\"utf-8\">",
    paste0("<title>", escape_html(title), "</title>"),
    "<style>",
    "body { font-family: -apple-system, BlinkMacSystemFont, 'Segoe UI', sans-serif; margin: 28px; color: #172026; }",
    "h1 { font-size: 22px; margin-bottom: 4px; }",
    "p.subtitle { color: #52616b; margin-top: 0; max-width: 1100px; line-height: 1.4; }",
    "table { border-collapse: collapse; width: 100%; font-size: 13px; }",
    "th { background: #f3f6f8; text-align: left; position: sticky; top: 0; }",
    "th, td { border: 1px solid #d9e1e7; padding: 7px 9px; vertical-align: top; }",
    "tr:nth-child(even) td { background: #fbfcfd; }",
    ".note { margin-top: 16px; color: #52616b; font-size: 12px; max-width: 1100px; line-height: 1.45; }",
    "</style>",
    "</head>",
    "<body>",
    paste0("<h1>", escape_html(title), "</h1>"),
    if (!is.null(subtitle)) paste0("<p class=\"subtitle\">", escape_html(subtitle), "</p>") else NULL,
    "<table>",
    paste0("<thead><tr>", header, "</tr></thead>"),
    "<tbody>",
    body,
    "</tbody>",
    "</table>",
    "<p class=\"note\">Generated by phase7_final_predictions_visualizations.R.</p>",
    "</body>",
    "</html>"
  )

  writeLines(html, path)
}

make_analysis_vars <- function(data) {
  data %>%
    mutate(
      cognitive_score = .data[[cog_var]],
      chronological_age = .data[[age_var]],
      age_c65 = chronological_age - 65,
      menopause_group = factor(menopause_group, levels = menopause_group_levels),
      menopause_group_label = factor(
